package classwork10;

public class Calculator {
    static double sum(int valueOne, int valueTwo){
        return valueOne + valueTwo;
    }

    static double minus(int valueOne, int valueTwo){
        return valueOne - valueTwo;
    }

    static double multiply(int valueOne, int valueTwo){
        return valueOne * valueTwo;
    }

    static double divide(int valueOne, int valueTwo){
        return valueOne / valueTwo;
    }
}
